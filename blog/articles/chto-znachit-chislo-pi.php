      <p class="txt_article"> И так давайте разберемся что же за зверь такой это число Пи. Возьмем что нибудь круглое,
         например апельсин (рис. 2) и замерим длину его окружности (L). Для этого удобнее всего разрезать апельсин
         пополам, взять веревку, обкинуть вокруг апельсина и приложить веревку к линейке. Предположим, у нас получилось
         34,5 сантиметра. Затем нам нужно измерить диаметр (d) апельсина, приложим линейку так чтобы она проходила через
         центр апельсина и возьмем отсчеты по линейке, диаметр получился примерно 11,0 сантиметра. Возьмем первое число
         L=34,5 и разделим его на второе d=11,0 и получим число равное 3,1364.</p>
