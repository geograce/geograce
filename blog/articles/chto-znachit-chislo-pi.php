      <p class="txt_article">Число Пи равное 3,14 это округленное значение, на самом деле это число бесконечно. Компания
         Google 14.03.2019 г. представила число Пи с 31,4 триллионами знаков после запятой, а вычислить его с такой
         точностью сумела Эмма Харука&ndash;Ивао из Японии. Число Пи иррациональное число и это значит, что его нельзя
         представить в виде отношения двух целых чисел, например 10/3, 3650/1150 и т.д. Другими словами, невозможно
         найти два целых числа, при делении которых одно на другое, давали бы число Пи. Как было бы здорово если бы
         такие числа были, взял одно число поставил в числитель, другое в знаменатель и получил число Пи, но природа нам
         такого не подарила подарка, что и доказал Иоганн Ламберт в 1761 году. Интересно то, что в 1897 году, на
         заседании Генеральной Ассамблеи Индианы (США), хотели принять <em>Законопроект о числе Пи № 246</em> в котором
         хотели установить число Пи равном 3,2, дескать нечего возиться с этим хвостатым монстром, примем 3,2 и точка.
         На заседании присутствовал профессор Вальдо из Университета Пердью, который вмешался и благодаря которому
         данный закон так и не приняли. Вообще число Пи стало героем многочисленных шуток, анекдотов и даже ставили
         памятники этому числу.</p>
