      <p class="txt_article" style="text-align: center;">Девяносто два и шесть.</p>
      <br>
      <p class="txt_article">Часть людей знают или догадываются, что число Пи связано с окружностью. Там, где есть
         окружность или ее часть, рано или поздно появится число Пи. Если вспомнить формулы площадей и объемов различных
         круглых или частично круглых тел (шар, цилиндр, конус и т.д.), то все они содержат константу Пи (рис. 1а). В
         фигурах, изображенных на рисунке 1б круглых частей нет, а значит и числу Пи в них делать нечего.</p>
      <br>
