      <p class="txt_article">Теперь давайте возьмем другой круглый предмет, но меньшего размера &ndash; вишня. Точно
         измерить ее конечно проблематично, но мы попросили знакомого Смурфика, он нам все измерил и доложил, что длина
         окружности равна 2,5 см., а диаметр равен 0,8 см. Давайте теперь будем делить первое число на второе, и получим
         число равное 3,1250. Любопытства ради возьмем что нибудь очень большое и круглое. Предлагаю взять нашу родную
         планету &ndash; Земля, только резать мы ее не будем, т.к. нам на ней еще жить. Возьмем параметры земли из
         справочника, благо все уже измерили до нас, L=40075,017км, d=‭12756,2км‬. Не терпится уже разделить, давайте
         этои сделаем 40075,017/12756,2‬=3,1416.‬</p>
      <br>
      <p class="txt_article">Во всех трех случаях мы получили числа близких к числу Пи, в примере с Землей наше число
         почти совпадает с числом Пи с точностью до трех знаков. Если все наши предметы (яблоко, вишня, Земля) были бы
         более правильной формой, а измеряли мы бы точными инструментами, то мы получили бы точное число Пи. Думаю,
         теперь всем стало ясно, что число Пи &ndash; это отношение длины окружности к ее диаметру (Пи=L/d). Совершенно
         не имеет значения какого размера эта окружность, это отношение всегда одинаково для любых окружностей и равно
         3,14. Можно сказать так &ndash; что длина окружности всегда больше диаметра в 3,14 раза и это одно из
         фундаментальных свойств природы, в которой мы живем. Фактически мы написали формулу для нахождения длины
         окружности, если взять диаметр и умножить на число Пи, то мы получим длину окружности. Если нам не будет
         известно число Пи, то мы никогда не сможем вычислить длины, площади и объемы разных фигур, где присутствует
         окружность.</p>
