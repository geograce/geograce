         Пленка в панорамном аэрофотоаппарате во время сканирования находится в неподвижном состоянии, а перед ее
         поверхностью движется щель АВ (рис. 2) и одновременно, как одно целое с ней, вращается объектив так, что его
         оптическая ось все время перпендикулярна к плоскости щели. Существуют также панорамный аэрофотоаппарат, в
         которых объектив неподвижен, а сканирование осуществляется при помощи вращающихся призм или зеркал.
         Преимущество данного аппарата является большой угол поля зрения это позволяет ему фотографировать широкие
         полосы местности, от горизонта до горизонта в стороны от линии пролет. Используются такие аппараты для
