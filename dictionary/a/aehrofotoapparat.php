         расстояния объектива. В СССР у современных аэрофотоаппаратах использовались следующие параметры
         аэрофотоснимков: 18x18 и 30x30 см. По величине фокусного расстояния аэрофотоаппарат подразделяется на
         короткофокусные (f до 150 мм), среднефокусные (f от 150 &ndash; 300 мм) и длиннофокусные (f более 300 мм).</p>
      <p class="txt_description_general">Основные составляющие аэрофотоаппарата:</p>
      <p class="list_description_general" style="padding-bottom: 0;"><strong>1. аэрофотокамера</strong> &ndash;
         цельнометаллический корпус, который соединяет в единое целое конус и кассету, также некоторые аэрофотоаппараты
         могут иметь два&ndash;три сменных конуса с объективами различных фокусных расстояний;</p>
      <p class="list_description_general" style="padding-bottom: 0;"><strong>2. кассета</strong> в которую помещается 60
         метров аэропленки шириной 19 или 30 см, что позволяет получить 300 аэрофотоснимков размером 18x18 см или 190
         размером 30x30 см, также есть кассеты на 120 метров аэропленки шириной 19 см.</p>
      <p class="list_description_general" style="padding-bottom: 0;"><strong>3. аэрофотоустановка</strong> 3.
         аэрофотоустановка (с амортизационным приспособлением) служит для укрепления аэрофотоаппарата на летательном
         аппарате и поглощения вибрации, которая происходит при работе винтомоторной группы самолета, также применяются
         гиростабилизирующие аэрофотоустановки, в основу которых лежит трехстепенной гироскоп, что позволяет значительно
         уменьшить угол отклонения оптической оси аэрофотоаппарата от отвесного положения;</p>
      <p class="list_description_general"><strong>4. командный прибор</strong> &ndash; автоматическое устройство,
