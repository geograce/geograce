      <p class="list_description_general"><strong>1. Аэрофотоаппарат щелевой</strong> у которого фотографирование
         происходит не отдельными кадрами, как на топографическом, а непрерывно на пленку, которая движется позади
         неподвижной узкой щели со скоростью, равной скорости движения объекта на местности, и в каждый момент
         фотографируется только узкая полоса местности поперек линии полета. Регулировка экспозиции выполняется
