      <p class="txt_description_general">Перспективной же аэрофотосъемкой можно сфотографировать эту полосу, не долетая
         даже до нее (точка фотографирования S'), причем будет сфотографирована более широкая полоса A'B'. В
         разведывательных целях центральная полоса маршрута нередко покрывается плановыми, а боковые его части &ndash;
         перспективными аэрофотоснимками. Для этого используют специальную аэрофотоустановку, которая называется
         качающейся установкой, при помощи которой камера может принимать попеременно отвесное и отклоненное на
         некоторый заданный угол положение, что позволяет за один маршрут сфотографировать широкую полосу местности.</p>
      <p class="txt_description_general">В наше время аэрофотосъемка полностью перешла на цифровые аэрофотосистемы, тем
         самым заменив аналоговые (пленочные) аэрофотоаппараты. Современные цифровые аэрофотосистемы могут одновременно
         выполнять съемку в панхроматическом, красном, синем, зеленом и ближнем инфракрасном спектральных диапазонах.
         Главные преимущества цифровой аэрофотосъемки перед аналоговой:</p>
      <p class="list_description_general" style="padding-bottom:0;">&bull; отсутствие специальных фотолабораторий, где происходила фотохимическая
         обработка снимков;</p>
      <p class="list_description_general" style="padding-bottom:0;">&bull; отсутствие сканирования аэрофотоснимков, т.к. снимки уже в цифровом
         виде;</p>
      <p class="list_description_general" style="padding-bottom:0;">&bull; нет геометрических искажений аэрофотоснимков, т.к. отсутствует
         деформация фотоприемника (матрица);</p>
      <p class="list_description_general" style="padding-bottom:0;">&bull; при фотограмметрической обработке отсутствует необходимость
         ориентирования снимка по координатным меткам;</p>
      <p class="list_description_general">&bull; на аэрофотоснимках отсутствуют механические повреждения, а также пыли и
         др.</p>
