      <h1 class="h1_description_general">Аэрофотоснимок</h1>
      <p class="txt_description_general"><strong>АЭРОФОТОСНИМОК</strong> &ndash; фотографическое изображение местности,
         которое было получено с самолета или другого летательного аппарата. В геометрическом отношении аэрофотоснимок
         представляет собой центральную (или иную) проекцию, центром проектирования которой служит задняя узловая точка
         объектива аэрофотоаппарата. В зависимости от вида аэрофотосъемки, типа аэрофотоаппарата, которым производилась
         аэрофотосъемка, и последующей обработки рассматриваются аэрофотоснимки следующих наименований.</p>
